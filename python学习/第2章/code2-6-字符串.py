# 创建字符串
# 单引号
s1 = 'hello'
print(s1)
# 双引号
s2 = "hello"
print(s2)
# 三引号
s3 = '''2024
hello
world
'''
print(s3)
# 引号的嵌套使用
s4 = "It's a hat"
print(s4)
# 添加转义字符
s5 = '1235\'\"6666'
print(s5)

# 字符串的拼接
print('----------字符串拼接---------')
print(s1 + s4)

# 字符串的重复

print('-----------字符串乘法---------')
print(s1 * 4)
print('*' * 20)
print('@' * 20)
print('happy' * 6)

